    def test_all_documented_images_have_handwritten_directories(self) -> None:
        """文档列出的全部镜像都必须有目录和生产清单,Dockerfile 由来源类型决定。"""
        missing: list[str] = []
        for category, name, image_dir in self.required_image_dirs():
            for filename in ("manifest.yaml", "README.md"):
                if not (image_dir / filename).is_file():
                    missing.append(f"{category}/{name}/{filename}")
            if not (image_dir / "manifest.yaml").is_file():
                continue
            manifest = self.load_manifest(image_dir / "manifest.yaml")
            source_type = manifest.get("source", {}).get("type")
            if source_type in DOCKERFILE_SOURCE_TYPES and not (image_dir / "Dockerfile").is_file():
                missing.append(f"{category}/{name}/Dockerfile")
            if source_type == "upstream-pinned" and (image_dir / "Dockerfile").exists():
                missing.append(f"{category}/{name}/Dockerfile 不应存在,纯上游固定镜像只登记不重打包")
        self.assertEqual([], missing)

