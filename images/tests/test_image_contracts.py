    def test_python_sources_follow_image_runtime_contract(self) -> None:
        """Python 镜像入口必须有职责注释、显式错误处理,且 HTTP 端口来自环境变量。"""
        errors: list[str] = []
        for path in IMAGES_ROOT.rglob("*.py"):
            if "__pycache__" in path.parts:
                continue
            relative = path.relative_to(IMAGES_ROOT)
            tree = ast.parse(path.read_text(encoding="utf-8"), filename=str(relative))
            if not ast.get_docstring(tree):
                errors.append(f"{relative}: 缺少文件头职责 docstring")
            for node in ast.walk(tree):
                if isinstance(node, (ast.FunctionDef, ast.AsyncFunctionDef, ast.ClassDef)):
                    if not ast.get_docstring(node):
                        errors.append(f"{relative}: {node.name} 缺少职责 docstring")
                if isinstance(node, ast.ExceptHandler) and node.type is None:
                    errors.append(f"{relative}: 存在裸 except")
                if isinstance(node, ast.Pass):
                    errors.append(f"{relative}: 存在 pass 空实现")
                if isinstance(node, ast.Call) and isinstance(node.func, ast.Name):
                    if node.func.id != "ThreadingHTTPServer" or not node.args:
                        continue
                    address = node.args[0]
                    if isinstance(address, ast.Tuple) and len(address.elts) == 2:
                        port = address.elts[1]
                        if isinstance(port, ast.Constant) and isinstance(port.value, int):
                            errors.append(f"{relative}: HTTP 服务端口必须来自环境变量")
                if isinstance(node, ast.Call) and isinstance(node.func, ast.Attribute):
                    if node.func.attr != "read":
                        continue
                    if isinstance(node.func.value, ast.Attribute) and node.func.value.attr == "rfile":
                        parent_function = self.enclosing_function_name(tree, node)
                        if parent_function == "do_POST" and not self.function_references_name(tree, node, "MAX_BODY_BYTES"):
                            errors.append(f"{relative}: POST 请求体读取必须先检查 MAX_BODY_BYTES")
        self.assertEqual([], errors)

    def enclosing_function_name(self, tree: ast.AST, target: ast.AST) -> str | None:
        """返回目标节点所在的最近函数名。"""
        result: str | None = None
        for node in ast.walk(tree):
            if not isinstance(node, ast.FunctionDef):
                continue
            for child in ast.walk(node):
                if child is target:
                    result = node.name
                    break
        return result

    def function_references_name(self, tree: ast.AST, target: ast.AST, name: str) -> bool:
        """检查目标节点所在函数是否引用指定名称。"""
        for node in ast.walk(tree):
            if not isinstance(node, ast.FunctionDef):
                continue
            if not any(child is target for child in ast.walk(node)):
                continue
            return any(isinstance(child, ast.Name) and child.id == name for child in ast.walk(node))
        return False

