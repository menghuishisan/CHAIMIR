    def test_manifest_declares_production_security_contract(self) -> None:
        """每个镜像必须声明生产安全、资源、自检和供应链门禁。"""
        errors: list[str] = []
        for category, name, image_dir in self.required_image_dirs():
            manifest = self.load_manifest(image_dir / "manifest.yaml")
            security = manifest.get("security", {})
            resources = manifest.get("resources", {})
            selftest = manifest.get("selftest", {})
            supply_chain = manifest.get("supply_chain", {})

            upstream_pinned = manifest.get("source", {}).get("type") == "upstream-pinned"
            expected_security = {
                "run_as_non_root": True,
                "allow_privilege_escalation": False,
                "privileged": False,
                "host_network": False,
                "automount_service_account_token": False,
            }
            for key, expected in expected_security.items():
                if security.get(key) != expected:
                    errors.append(f"{category}/{name}: security.{key}={security.get(key)!r}")
            if security.get("read_only_root_filesystem") is False:
                exceptions = manifest.get("security_exceptions", [])
                if not exceptions:
                    errors.append(f"{category}/{name}: 只读根文件系统例外缺少 security_exceptions")
            elif security.get("read_only_root_filesystem") is not True:
                errors.append(f"{category}/{name}: security.read_only_root_filesystem={security.get('read_only_root_filesystem')!r}")
            if "ALL" not in security.get("drop_capabilities", []):
                errors.append(f"{category}/{name}: security.drop_capabilities 缺少 ALL")
            for key in ("cpu_request", "cpu_limit", "memory_request", "memory_limit", "ephemeral_storage_limit"):
                if not resources.get(key):
                    errors.append(f"{category}/{name}: resources.{key} 缺失")
            if not selftest.get("commands") and not (category == "base" and selftest.get("note")) and not upstream_pinned:
                errors.append(f"{category}/{name}: selftest.commands 缺失")
            for key in ("sbom", "trivy_required", "cosign_required", "pull_by_digest_in_production"):
                if supply_chain.get(key) is not True:
                    errors.append(f"{category}/{name}: supply_chain.{key} 不是 true")
        self.assertEqual([], errors)

    def test_manifest_declares_image_source_contract(self) -> None:
        """每个镜像必须声明来源类型,Dockerfile 与上游锁定契约由来源类型决定。"""
        errors: list[str] = []
        for category, name, image_dir in self.required_image_dirs():
            manifest = self.load_manifest(image_dir / "manifest.yaml")
            source = manifest.get("source", {})
            source_type = source.get("type")
            if source_type not in VALID_SOURCE_TYPES:
                errors.append(f"{category}/{name}: source.type 非法或缺失")
                continue
            if source_type == "upstream-pinned":
                if (image_dir / "Dockerfile").exists():
                    errors.append(f"{category}/{name}: upstream-pinned 不得维护 Dockerfile")
                upstream = manifest.get("upstream", {})
                for key in ("image", "version", "registry", "license"):
                    if not upstream.get(key):
                        errors.append(f"{category}/{name}: upstream.{key} 缺失")
                if upstream.get("digest_required") is not True:
                    errors.append(f"{category}/{name}: upstream.digest_required 必须为 true")
                digest = upstream.get("digest")
                lock_ref = upstream.get("digest_lock_ref")
                components = upstream.get("components", [])
                has_locked_component = any(component.get("digest") for component in components if isinstance(component, dict))
                if not digest and not lock_ref and not has_locked_component:
                    errors.append(f"{category}/{name}: upstream 必须以内联 digest、digest_lock_ref 或组件 digest 锁定")
                if digest and not re.fullmatch(r"sha256:[0-9a-f]{64}", str(digest)):
                    errors.append(f"{category}/{name}: upstream.digest 格式非法")
                delivery = manifest.get("private_delivery", {})
                if delivery.get("offline_bundle") is not True:
                    errors.append(f"{category}/{name}: private_delivery.offline_bundle 必须为 true")
                if not delivery.get("import_project"):
                    errors.append(f"{category}/{name}: private_delivery.import_project 缺失")
                if manifest.get("student_access", {}).get("enabled") is not False:
                    errors.append(f"{category}/{name}: 上游中间件/入口/观测镜像不得声明学生入口")
                for key in ("secrets_required", "data_volumes", "backup", "network"):
                    if key not in manifest:
                        errors.append(f"{category}/{name}: {key} 缺失")
            if source_type in DOCKERFILE_SOURCE_TYPES and not (image_dir / "Dockerfile").is_file():
                errors.append(f"{category}/{name}: {source_type} 必须维护 Dockerfile")
            if source_type == "thin-wrapper":
                upstream = manifest.get("upstream", {})
                if not upstream.get("image"):
                    errors.append(f"{category}/{name}: thin-wrapper 必须声明 upstream.image")
        self.assertEqual([], errors)

