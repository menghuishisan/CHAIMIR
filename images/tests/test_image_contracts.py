    def test_manifest_env_keys_are_declared_in_deploy_env_source(self) -> None:
        """镜像需要的配置和密钥变量名必须统一来自 deploy/config/chaimir.env。"""
        errors: list[str] = []
        deploy_keys = self.deploy_env_keys()
        for category, name, image_dir in self.required_image_dirs():
            manifest = self.load_manifest(image_dir / "manifest.yaml")
            env_keys = manifest.get("env_keys", {})
            if env_keys:
                if not isinstance(env_keys, dict):
                    errors.append(f"{category}/{name}: env_keys 必须是对象")
                for group in ("config", "secret"):
                    for key in env_keys.get(group, []):
                        if key not in deploy_keys:
                            errors.append(f"{category}/{name}: env_keys.{group} {key} 未在 deploy/config/chaimir.env 声明")
            for secret in manifest.get("secrets_required", []):
                env = secret.get("env")
                if not env:
                    errors.append(f"{category}/{name}: secrets_required 缺少 env")
                    continue
                if env not in deploy_keys:
                    errors.append(f"{category}/{name}: secrets_required {env} 未在 deploy/config/chaimir.env 声明")
                if secret.get("source") != "deploy/config/chaimir.env":
                    errors.append(f"{category}/{name}: secrets_required {env} source 必须为 deploy/config/chaimir.env")
                if secret.get("runtime_value") != "secret-or-kms":
                    errors.append(f"{category}/{name}: secrets_required {env} runtime_value 必须为 secret-or-kms")
        self.assertEqual([], errors)

